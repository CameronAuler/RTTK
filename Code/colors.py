"""This module holds all of the colors that are used to color the UI for the RTTK project."""
class Colors:
    """This class contains all of the terminal customization techniques for the RTTK project."""
    red = "\u001b[0;31m"
    green = "\u001b[0;32m"
    yellow = "\u001b[0;33m"
    blue = "\u001b[0;34m"
    magenta = "\u001b[0;35m"
    cyan = "\u001b[0;36m"
    white = "\u001b[0;37m"
    underline = "\u001b[0;4m"
    bold = "\u001b[0;1m"
    inverse = "\u001b[0;7m"
    end = "\u001b[0;0m"
